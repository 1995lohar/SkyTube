package free.rm.skytube.gui.fragments;

import android.app.Activity;
import android.net.Uri;
import android.os.Bundle;
import android.app.Fragment;
import android.view.LayoutInflater;
import android.view.View;
import android.view.ViewGroup;
import android.widget.GridView;

import free.rm.skytube.R;
import free.rm.skytube.gui.businessobjects.GridAdapter;

/**
 *
 */
public class FeaturedVideosFragment extends FragmentEx {

	private GridAdapter gridAdapter = null;

	@Override
	public View onCreateView(LayoutInflater inflater, ViewGroup container, Bundle savedInstanceState) {
		// inflate the layout for this fragment
		View view = inflater.inflate(R.layout.fragment_featured_videos, container, false);

		GridView gridView = (GridView) view.findViewById(R.id.grid_view);

		if (gridAdapter == null) {
			gridAdapter = new GridAdapter(getActivity());
		}
		gridView.setAdapter(gridAdapter);

		return view;
	}

}
